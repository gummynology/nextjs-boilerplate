export function normalizeIngredientName(value: string) {
  return value
    .trim()
    .toLowerCase()
    .replace(/[^\w\s-]/g, " ")
    .replace(/\s+/g, " ");
}

export function getUniqueActiveIngredients(
  ingredients: ActiveIngredientDefinition[] = activeIngredients,
) {
  return Array.from(
    new Map(
      ingredients.map((ingredient) => [
        normalizeIngredientName(ingredient.name),
        ingredient,
      ]),
    ).values(),
  );
}

function buildIngredientLookup(ingredients: ActiveIngredientDefinition[]) {
  const ingredientLookup = new Map<string, ActiveIngredientDefinition>();

  ingredients.forEach((ingredient) => {
    ingredientLookup.set(normalizeIngredientName(ingredient.name), ingredient);
    ingredient.aliases?.forEach((alias) => {
      ingredientLookup.set(normalizeIngredientName(alias), ingredient);
    });
  });

  return ingredientLookup;
}

export const uniqueActiveIngredients = getUniqueActiveIngredients();
const ingredientLookup = buildIngredientLookup(uniqueActiveIngredients);

export function findActiveIngredient(
  value: string,
  ingredients: ActiveIngredientDefinition[] = uniqueActiveIngredients,
) {
  if (ingredients === uniqueActiveIngredients) {
    return ingredientLookup.get(normalizeIngredientName(value)) ?? null;
  }

  return (
    buildIngredientLookup(getUniqueActiveIngredients(ingredients)).get(
      normalizeIngredientName(value),
    ) ?? null
  );
}

export function searchActiveIngredients(
  query: string,
  ingredients: ActiveIngredientDefinition[] = uniqueActiveIngredients,
) {
  const normalizedQuery = normalizeIngredientName(query);
  const uniqueIngredients = getUniqueActiveIngredients(ingredients);

  if (!normalizedQuery) {
    return uniqueIngredients;
  }

  return uniqueIngredients.filter((ingredient) => {
    const searchableValues = [
      ingredient.name,
      ingredient.category,
      ...(ingredient.aliases ?? []),
    ].map(normalizeIngredientName);

    return searchableValues.some((value) => value.includes(normalizedQuery));
  });
}
